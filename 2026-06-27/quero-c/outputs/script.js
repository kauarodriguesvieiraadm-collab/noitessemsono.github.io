let currentLetter = 0;

const links = [...document.querySelectorAll(".letter-link")];
const counter = document.getElementById("counter");
const reader = document.getElementById("reader");
const letterTitle = document.getElementById("letterTitle");
const letterBody = document.getElementById("letterBody");
const letterCount = document.getElementById("letterCount");
const rainToggle = document.getElementById("rainToggle");

let audioContext;
let rainGain;
let rainSource;
let rainFilter;
let rainEnabled = false;

function setCurrentLetter(index) {
  currentLetter = (index + letters.length) % letters.length;
  links.forEach((link, linkIndex) => {
    link.classList.toggle("is-active", linkIndex === currentLetter);
  });
  counter.textContent = `${currentLetter + 1} / ${letters.length}`;
}

function renderLetter() {
  const letter = letters[currentLetter];
  letterTitle.textContent = letter.title;
  letterCount.textContent = `Carta ${currentLetter + 1} de ${letters.length}`;
  letterBody.innerHTML = "";

  letter.paragraphs.forEach((text) => {
    const paragraph = document.createElement("p");
    if (text.startsWith("*") && text.endsWith("*")) {
      const emphasis = document.createElement("em");
      emphasis.textContent = text.slice(1, -1);
      paragraph.append(emphasis);
    } else {
      paragraph.textContent = text;
    }
    letterBody.append(paragraph);
  });
}

function openReader() {
  renderLetter();
  reader.classList.add("is-open");
  reader.setAttribute("aria-hidden", "false");
  document.body.style.overflow = "hidden";
}

function closeReader() {
  reader.classList.remove("is-open");
  reader.setAttribute("aria-hidden", "true");
  document.body.style.overflow = "";
}

function createRainBuffer(context) {
  const duration = 3;
  const frameCount = context.sampleRate * duration;
  const buffer = context.createBuffer(1, frameCount, context.sampleRate);
  const data = buffer.getChannelData(0);

  for (let index = 0; index < frameCount; index += 1) {
    const softNoise = Math.random() * 2 - 1;
    const drop = Math.random() > 0.993 ? Math.random() * 0.72 : 0;
    data[index] = softNoise * 0.22 + drop;
  }

  return buffer;
}

function setupRainAudio() {
  if (audioContext) {
    return;
  }

  const AudioContextClass = window.AudioContext || window.webkitAudioContext;
  if (!AudioContextClass) {
    rainToggle.textContent = "Chuva indisponível";
    rainToggle.disabled = true;
    return;
  }

  audioContext = new AudioContextClass();
  rainSource = audioContext.createBufferSource();
  rainSource.buffer = createRainBuffer(audioContext);
  rainSource.loop = true;

  rainFilter = audioContext.createBiquadFilter();
  rainFilter.type = "bandpass";
  rainFilter.frequency.value = 1050;
  rainFilter.Q.value = 0.55;

  const lowpass = audioContext.createBiquadFilter();
  lowpass.type = "lowpass";
  lowpass.frequency.value = 2300;

  rainGain = audioContext.createGain();
  rainGain.gain.value = 0;

  rainSource.connect(rainFilter);
  rainFilter.connect(lowpass);
  lowpass.connect(rainGain);
  rainGain.connect(audioContext.destination);
  rainSource.start();
}

async function toggleRain() {
  setupRainAudio();
  if (!audioContext) {
    return;
  }

  if (audioContext.state === "suspended") {
    await audioContext.resume();
  }

  rainEnabled = !rainEnabled;
  const now = audioContext.currentTime;
  rainGain.gain.cancelScheduledValues(now);
  rainGain.gain.setValueAtTime(rainGain.gain.value, now);
  rainGain.gain.linearRampToValueAtTime(rainEnabled ? 0.045 : 0, now + 0.55);

  document.body.classList.toggle("rain-on", rainEnabled);
  rainToggle.setAttribute("aria-pressed", String(rainEnabled));
  rainToggle.textContent = rainEnabled ? "Chuva ligada" : "Chuva baixa";
}

links.forEach((link) => {
  link.addEventListener("click", () => {
    setCurrentLetter(Number(link.dataset.letter));
    openReader();
  });
});

document.getElementById("openLetter").addEventListener("click", openReader);
rainToggle.addEventListener("click", toggleRain);
document.getElementById("prevLetter").addEventListener("click", () => {
  setCurrentLetter(currentLetter - 1);
  renderLetter();
});
document.getElementById("nextLetter").addEventListener("click", () => {
  setCurrentLetter(currentLetter + 1);
  renderLetter();
});

document.querySelectorAll("[data-close]").forEach((button) => {
  button.addEventListener("click", closeReader);
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeReader();
  }

  if (!reader.classList.contains("is-open")) {
    return;
  }

  if (event.key === "ArrowLeft") {
    setCurrentLetter(currentLetter - 1);
    renderLetter();
  }

  if (event.key === "ArrowRight") {
    setCurrentLetter(currentLetter + 1);
    renderLetter();
  }
});
